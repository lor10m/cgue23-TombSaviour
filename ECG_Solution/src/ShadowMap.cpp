#include "ShadowMap.h"

void ShadowMap::create()
{

	shader.createPhongShader("assets/textures/brick.jpg", "assets/textures/brick_specular.jpg", false, glm::mat4(1.0f), 0.05f, 0.8f, 0.5f, 1.0f);
	depthShader.createDepthMapShader();
	//debugShader.createDebugShadowShader();

	//Load Model/Mesh
	createPlane();


	// configure depth map FBO
	// -----------------------
	glGenFramebuffers(1, &depthMapFBO);

	// create depth texture
	glGenTextures(1, &depthMap);
	glBindTexture(GL_TEXTURE_2D, depthMap);
	glTexImage2D(GL_TEXTURE_2D, 0, GL_DEPTH_COMPONENT, SHADOW_WIDTH, SHADOW_HEIGHT, 0, GL_DEPTH_COMPONENT, GL_FLOAT, NULL);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_BORDER);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_BORDER);
	float borderColor[] = { 1.0f, 1.0f, 1.0f, 1.0f };
	glTexParameterfv(GL_TEXTURE_2D, GL_TEXTURE_BORDER_COLOR, borderColor);

	// attach depth texture as FBO's depth buffer
	glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
	glFramebufferTexture2D(GL_FRAMEBUFFER, GL_DEPTH_ATTACHMENT, GL_TEXTURE_2D, depthMap, 0);
	glDrawBuffer(GL_NONE);
	glReadBuffer(GL_NONE);
	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	// Matrices needed for the light's perspective

	lightColor = glm::vec4(1.0f, 1.0f, 1.0f, 1.0f); //nicht verwendet im shader
	lightPos = glm::vec3(-1.2f, 29.0f, 2.0f);
	glm::mat4 orthgonalProjection = glm::ortho(-10.0f, 10.0f, -10.0f, 10.0f, 1.0f, 7.5f);
	glm::mat4 lightView = glm::lookAt(lightPos, glm::vec3(0.0f), glm::vec3(0.0f, 1.0f, 0.0f));
	lightSpaceMatrix = orthgonalProjection * lightView;

	// shader configuration
	// --------------------
	shader.setUniform1i("diffuseTexture", 0);
	shader.setUniform1i("specularTexture", 1);
	shader.setUniform1i("shadowMap", 2);

}

void ShadowMap::render(glm::mat4 viewMatrix, glm::vec3 eyePos)
{

	glEnable(GL_CULL_FACE);

	// 1. render depth of scene to texture (from light's perspective)
	// --------------------------------------------------------------
	glViewport(0, 0, SHADOW_WIDTH, SHADOW_HEIGHT);
	glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);

	// Draw model/mesh with shadowMap shader
	depthShader.setUniformMatrix4fv("lightSpaceMatrix", 1, GL_FALSE, lightSpaceMatrix);
	renderScene(depthShader, true);


	// Switch back to the default framebuffer
	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	// reset viewport
	glViewport(0, 0, 800, 800);
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	// 2. render scene as normal using the generated depth/shadow map  
	// --------------------------------------------------------------
	shader.activate();
	shader.setUniformMatrix4fv("lightSpaceMatrix", 1, GL_FALSE, lightSpaceMatrix);
	shader.setUniform3f("lightPos", lightPos.x, lightPos.y, lightPos.z);
	shader.setUniformMatrix4fv("viewMatrix", 1, GL_FALSE, viewMatrix);
	shader.setUniform3f("eyePos", eyePos.x, eyePos.y, eyePos.z);
	shader.setUniform1i("isAnimated", 0);


	glActiveTexture(GL_TEXTURE0 + 2);
	glBindTexture(GL_TEXTURE_2D, depthMap);
	shader.setUniform1i("shadowMap", 2);

	renderScene(shader, false);

	//debugShader.setUniform1f("near_plane", 1.0f);
	//debugShader.setUniform1f("far_plane", 7.5f);
	//debugShader.setUniform1i("depthMap", 0);
	//glActiveTexture(GL_TEXTURE0);
	//glBindTexture(GL_TEXTURE_2D, depthMap);
	//renderDebug();

	
}

void ShadowMap::renderScene(Shader& shader, bool cull)
{
	glDisable(GL_CULL_FACE);

	// floor
	glm::mat4 model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(0.0f, 25.64, 0.0));
	shader.setUniformMatrix4fv("modelMatrix", 1, GL_FALSE, model);
	glBindVertexArray(planeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 6);
	glBindVertexArray(0); //TODO Maybe not


	if (cull) {
		glEnable(GL_CULL_FACE);
		glCullFace(GL_FRONT);
	}

	// cubes
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(0.0f, 1.5f + 25.64, 0.0));
	model = glm::scale(model, glm::vec3(0.5f));
	shader.setUniformMatrix4fv("modelMatrix", 1, GL_FALSE, model);
	renderCube();


	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(2.0f, 0.0f + 25.64, 1.0));
	model = glm::scale(model, glm::vec3(0.5f));
	shader.setUniformMatrix4fv("modelMatrix", 1, GL_FALSE, model);
	renderCube();


	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(-1.0f, 0.0f + 25.64, 2.0));
	model = glm::rotate(model, glm::radians(60.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
	model = glm::scale(model, glm::vec3(0.25));
	shader.setUniformMatrix4fv("modelMatrix", 1, GL_FALSE, model);
	renderCube();

}

